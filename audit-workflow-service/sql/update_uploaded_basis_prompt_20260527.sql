## 审核标准

1. **应检查文件中的核心标识信息是否与权威依据材料保持一致。**

   - 判断方法：比对文件中的编号、任务号、报告号、合同号、样品号、申请号等核心标识，与系统记录、任务书、委托书、合同、证书、附件或其他权威来源是否一致。允许格式差异，但不允许实质编号差异。
   - 不合格示例：报告中的任务编号为“A12345”，任务书中的编号为“A12346”；报告编号缺少规定的前缀或后缀。
   - 输出建议：指出不一致字段、文件中的实际值、依据材料中的标准值、差异位置，并建议按权威依据材料修正。

2. **应检查文件中的主体对象信息是否与依据材料一致。**

   - 判断方法：比对文件中的对象名称、产品名称、项目名称、型号规格、版本号、单位名称、申请主体、生产主体、地址、联系人、联系方式等信息，与委托书、合同、证书、说明书、标准文件、系统记录等来源是否一致。
   - 不合格示例：正文中的产品型号与封面型号不一致；报告中的申请单位与委托材料中的申请单位不同。
   - 输出建议：按“字段名称—文件值—依据值—差异说明”的方式输出，并标明建议以哪个来源为准。

3. **应检查同一信息在文件内部不同位置是否一致。**

   - 判断方法：对封面、摘要、正文、结论页、附表、目录、附件、页眉页脚等位置反复出现的同一字段进行内部一致性比对。
   - 不合格示例：封面写明型号为“X-100”，正文写为“X-100A”；结论页中的单位名称与首页不一致。
   - 输出建议：列出所有出现该字段的位置及对应值，指出冲突项，并建议统一为经核验的正确值。

4. **应检查多个依据来源之间是否存在优先级，并按优先级进行判断。**

   - 判断方法：当同一字段可从多个材料中获取时，应优先采用制度、合同、证书、委托书、系统记录或文件要求中明确优先的来源；若无明确优先级，应识别并提示来源冲突。
   - 不合格示例：系统记录、委托书和证书中的型号不同，但文件未按指定优先来源填写，也未说明原因。
   - 输出建议：说明存在多个来源值，列出各来源的值，标注推荐依据来源，并提示需要人工确认或按优先级修正。

5. **应检查文件类型、业务类别、申请类别或检验类别是否与依据材料和正文内容一致。**

   - 判断方法：核对文件声明的类别、类型、用途、办理事项、检验性质、审查类型等是否与委托材料、任务书、合同、规则要求及正文内容相匹配。
   - 不合格示例：委托材料显示为“变更检验”，报告中却写成“首次检验”；文件结论中的业务类别与封面类别不一致。
   - 输出建议：指出类别字段不一致的位置，并说明应依据哪个来源或规则确定最终类别。

6. **应检查文件引用的法规、标准、规则、制度、技术文件或企业文件是否准确完整。**

   - 判断方法：比对文件中引用的标准编号、文件名称、版本号、发布日期、实施日期、规则代号、企业标准代号等是否与依据材料一致，且名称与编号应成对匹配。
   - 不合格示例：标准编号正确但标准名称错误；文件引用了过期版本；结论页引用的规则代号与委托书中的规则不一致。
   - 输出建议：输出错误引用项、文件中的引用内容、正确引用内容，并建议修正编号、名称或版本。

7. **应检查引用依据与文件适用范围是否匹配。**

   - 判断方法：判断文件采用的标准、条款、规则或审查依据是否适用于当前对象、业务类别、技术类别或检验范围，避免引用不相关或不适用的依据。
   - 不合格示例：文件属于某一类产品或事项，却引用了另一类别的专用规则；正文使用的条款与报告声明的检验范围不匹配。
   - 输出建议：说明当前文件适用范围、已引用依据、疑似不适用原因，并建议更换或补充正确依据。

8. **应检查结论性表述是否与依据、正文和实际结果一致。**

   - 判断方法：核对结论页、摘要、审批意见、最终判定等结论性文字中涉及的对象名称、类别、依据、项目范围、结果状态、页码范围等是否与正文和依据材料一致。
   - 不合格示例：正文存在未通过项目，但结论写为“全部合格”；结论中引用的依据与正文检验依据不同。
   - 输出建议：指出结论与正文或依据的冲突点，并建议按详细结果重新生成或修正结论。

9. **应检查文件中的页码、章节号、表格号、附件号和交叉引用是否准确。**

   - 判断方法：核对“见第X页”“详见第X章”“见附件X”“结果见表X”等交叉引用是否真实存在，且引用位置与被引用内容一致。
   - 不合格示例：结论写“详见第3—20页”，但实际结果页为第3—18页；样品描述写“明细表见第21页”，但明细表实际在第22页。
   - 输出建议：输出错误引用文本、实际所在位置、正确引用方式，并建议更新页码或章节编号。

10. **应检查目录、项目清单、索引表与正文内容是否一一对应。**

    - 判断方法：比对目录、检验项目表、审查项目表、问题清单、索引表中的项目名称、序号、页码、结论，与正文对应项目是否一致。

    - 不合格示例：目录列出15项，正文只有14项；项目清单中的第8项页码指向了第9项内容。

    - 输出建议：列出缺失、重复、错位或页码错误的项目，并建议重新核对目录或项目清单。

11. **应检查项目数量、序号范围和统计值是否与正文实际内容一致。**

    - 判断方法：核对文件中“共X项”“合计X项”“序号最大值”“项目数量”等统计信息，是否与正文实际列出的项目数量一致。

    - 不合格示例：报告写“共15项”，但正文实际列出16个检验项目；最大序号为18，但统计栏写17。

    - 输出建议：输出统计值、实际计数值、差异数量，并建议按正文实际项目数修正。

12. **应检查关键日期之间是否满足时间逻辑。**

    - 判断方法：核对申请日期、委托日期、送样日期、到样日期、检验日期、审核日期、签发日期、批准日期等是否符合先后顺序和业务规则。

    - 不合格示例：检验日期早于到样日期；报告签发日期早于检验完成日期；审核日期早于申请日期。

    - 输出建议：列出相关日期链条，指出违反的时间逻辑，并建议核实或修正对应日期。

13. **应检查有效期、校准期、授权期、证书期等是否覆盖文件中的使用日期。**

    - 判断方法：核对设备校准有效期、证书有效期、授权有效期、资质有效期等，是否晚于或覆盖实际使用、检验、审查或签发日期。

    - 不合格示例：设备在检验当天已超过校准有效期；引用证书在报告签发前已失效。

    - 输出建议：输出有效期字段、实际使用日期、超期情况，并建议更换有效依据或补充说明。

14. **应检查样品、对象或项目编号的构成是否符合文件内部规则和外部依据。**

    - 判断方法：判断编号是否由任务号、委托号、批次号、样品序号、类别后缀等要素组成，且组成规则与任务书、系统记录、报告说明或业务规则一致。

    - 不合格示例：样品编号中的委托号部分与首页委托编号不一致；编号缺少应有的子样品序号。

    - 输出建议：拆解编号构成，指出哪一部分不一致或缺失，并建议按编号规则重组。

15. **应检查对象描述、样品描述或项目描述是否与来源材料一致。**

    - 判断方法：比对文件中的材质、尺寸、结构、组成、配置、规格、等级、用途、关键部件、特殊说明等描述，与说明书、技术文件、标准文件、明细表或证书是否一致。

    - 不合格示例：说明书中的外形尺寸为“300×200×100”，报告描述为“300×210×100”；文件写“无特殊部件”，但明细表列有特殊部件。

    - 输出建议：输出描述项、文件值、依据值和差异，并建议按权威技术资料修正。

16. **应检查明细表、附件清单、受控部件清单或材料清单是否完整一致。**

    - 判断方法：核对文件中的清单项目、名称、型号、规格、生产单位、有效期、数量、备注等是否与来源清单一致，是否存在漏项、错项、重复项或多余项。

    - 不合格示例：来源清单中有关键部件，但报告明细表遗漏；报告中列出的部件型号与附件清单不一致。

    - 输出建议：按“缺失项、错误项、多余项、重复项”分类输出，并建议同步更新清单。

17. **应检查“适用、不适用、有、无、通过、不通过、P、N/A”等状态标记是否有依据。**

    - 判断方法：结合来源材料、技术条款、对象特征和上下文，判断状态标记是否合理；不能仅看单个标记，应核对其触发条件。

    - 不合格示例：来源材料显示存在某功能或部件，但文件对应条款标为“不适用”；正文描述有该项目，但结论标为“N/A”。

    - 输出建议：说明状态标记、触发依据、实际条件和不一致原因，并建议改为正确状态或补充说明。

18. **应检查存在性依赖关系是否成立。**

    - 判断方法：若依据材料中出现某功能、部件、配置、风险、环境条件或试验要求，文件中应出现相应项目、设备、方法、结果或说明；若依据材料中不存在，则文件不应无依据增加相关内容。

    - 不合格示例：技术资料显示有电池，但文件中缺少电池相关说明或试验项目；资料中无激光功能，但设备清单中出现激光测试设备。

    - 输出建议：按“依据中存在但文件缺失”或“依据中不存在但文件多出”输出，并说明触发条件。

19. **应检查设备、工具、仪器或资源清单是否与实际项目相匹配。**

    - 判断方法：核对文件列出的设备、工具、仪器、系统、资源是否与已执行的项目、方法、条款或任务相对应，既不能遗漏必要设备，也不能出现明显无关设备。

    - 不合格示例：文件包含防水试验项目，但设备清单中没有对应防水测试设备；未进行某类测试却列出了相关专用设备。

    - 输出建议：指出项目与设备之间的缺失或多余关系，并建议补充、删除或说明。

20. **应检查设备、工具或方法的能力范围是否满足被审核项目要求。**

    - 判断方法：比对设备量程、精度、等级、适用条件、测试能力、方法适用范围等，与项目要求、标准条款或技术指标是否匹配。

    - 不合格示例：项目要求测量范围到1000V，但使用设备量程不足；测试方法适用于低温环境，却用于高温条件。

    - 输出建议：输出要求范围、实际设备或方法能力范围、差异点，并建议更换合适设备或方法。

21. **应检查试验方法、审查程序或判定准则是否根据条件正确选择。**

    - 判断方法：当不同等级、类别、参数、对象特征对应不同方法或判据时，应核对文件是否选择了与实际条件匹配的方法和判据。

    - 不合格示例：对象等级不同应采用不同试验方法，但文件沿用了默认方法；判定准则与前文确定的技术等级不匹配。

    - 输出建议：说明触发条件、应采用的方法、文件实际采用的方法，并建议按条件重新选择。

22. **应检查数值结果是否满足技术要求、限值或区间要求。**

    - 判断方法：识别“≤、≥、<、>、范围、最大值、最小值、允许误差”等要求，判断实际结果是否落入合格范围。

    - 不合格示例：要求“≤100”，实际结果为“120”；要求“≥50MΩ”，实际结果为“30MΩ”。

    - 输出建议：输出技术要求、实际结果、比较结论和是否超限，并建议标记为数值不符合或核实数据。

23. **应检查数值的单位、量纲、精度和表达格式是否一致且可比较。**

    - 判断方法：比对数值时应同时核对单位、倍率、精度、百分比、小数位、量纲转换等，避免只比较数字而忽略单位。

    - 不合格示例：依据要求为“100m”，文件写为“100mm”；标准要求单位为“MPa”，文件写成“kPa”但未换算。

    - 输出建议：输出原始数值和单位，说明是否需要换算，并指出单位错误或量纲不一致。

24. **应检查计算项、合计项、统计项是否由明细数据正确计算得出。**

    - 判断方法：对费用合计、数量合计、项目总数、分项汇总、平均值、比例、范围值等进行重新计算，判断是否存在计算错误。

    - 不合格示例：各分项金额相加为7600，但合计栏写为7500；明细共12项，汇总写成11项。

    - 输出建议：输出明细计算过程、文件合计值、复核计算值和差额，并建议修正汇总值。

25. **应检查单项结果、分项结论和最终结论是否逐级一致。**

    - 判断方法：核对每个项目的原始结果、单项判定、分项结论、总表结论、最终结论是否逻辑一致；若底层结果不合格，上层结论不应直接写合格。

    - 不合格示例：某单项结果显示“不符合要求”，但项目清单和最终结论均写“合格”。

    - 输出建议：指出底层结果与上层结论的冲突，并建议重新判定或补充说明。

26. **应检查必需项目是否均有完整的内容、结果、依据和结论。**

    - 判断方法：根据文件类型、目录、任务要求、标准条款、项目清单和来源材料，判断必需章节、字段、表格、结果、签署、附件是否缺失。

    - 不合格示例：项目清单中列有某项试验，但正文没有对应结果；文件有判定结论但缺少判定依据。

    - 输出建议：列出缺失内容的位置和应补充的要素，并标注为完整性问题。

27. **应检查不适用、缺失、豁免或例外情况是否给出合理说明。**

    - 判断方法：当文件中出现“不适用”“无”“未检”“缺失”“豁免”“暂不提供”等情形时，应检查是否说明原因，并判断原因是否与依据材料一致。

    - 不合格示例：文件将某项目标为“不适用”，但未说明原因；依据材料显示该项目适用，文件却直接省略。

    - 输出建议：要求补充不适用依据或原因；若与依据冲突，应标记为逻辑不一致。

28. **应检查文件中关键表格的表头、字段和栏目是否完整规范。**

    - 判断方法：核对表格是否包含完成审核所必需的字段，如序号、项目名称、依据、要求、结果、结论、页码、备注等；字段名称应清晰且含义一致。

    - 不合格示例：结果表缺少“结论”列；清单表有序号但没有对应页码或依据。

    - 输出建议：指出缺失或不规范的栏目，并建议补充字段或统一表头命名。

29. **应检查文件中的专有名词、编号、代码、型号、标准号等是否精确一致。**

    - 判断方法：对专有名词、标准号、型号、证书号、单位名称、技术参数代号等进行高精度比对，注意大小写、连字符、括号、空格、上下标、罗马数字、全半角等差异。

    - 不合格示例：型号中的“-S1”被漏写；标准号中的年份错误；单位名称少写分公司或括号内容。

    - 输出建议：输出精确差异，并说明该类字段不宜仅按语义相近判断，应按原文或依据材料修正。

30. **应检查文件中的自然语言描述是否与数据、表格和结论相互支撑。**

    - 判断方法：比对文字描述、表格数据、项目结果、附件材料之间是否存在矛盾；文字不能声称某事项成立而数据不支持。

    - 不合格示例：文字写“全部项目符合要求”，但表格中有项目未填写或结果异常；描述写“无某部件”，明细表却列出了该部件。

    - 输出建议：指出文本与数据的矛盾，并建议修改描述或核实底层数据。

31. **应检查文件内容是否存在重复、遗漏、错位或拼接错误。**

    - 判断方法：审查连续编号、表格行、项目段落、页眉页脚、附件引用、复制粘贴内容，判断是否出现重复段落、错位字段、上一项目内容误放到下一项目等问题。

    - 不合格示例：第5项和第6项内容完全重复；某项目的结论出现在另一个项目下方；页眉中的报告名称与正文对象不一致。

    - 输出建议：指出疑似重复或错位位置，并建议回到原始文件核对排版和内容归属。

32. **应检查文件中的格式差异是否影响实质含义。**

    - 判断方法：对空格、换行、标点、全角半角、大小写、缩进、字体等差异进行区分：不影响含义的格式差异可不判为错误；影响编号、数值、单位、名称识别的格式差异应判为问题。

